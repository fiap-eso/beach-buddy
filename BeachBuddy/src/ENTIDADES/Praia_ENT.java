package ENTIDADES;

public class Praia_ENT {
	
	public Integer id_praia;
	public String nome_praia;
	public String cidade_praia;
	public String estado_praia;
	public Integer status_praia;
	
	
	public Integer getId_praia() {
		return id_praia;
	}
	public void setId_praia(Integer id_praia) {
		this.id_praia = id_praia;
	}
	public String getNome_praia() {
		return nome_praia;
	}
	public void setNome_praia(String nome_praia) {
		this.nome_praia = nome_praia;
	}
	public String getCidade_praia() {
		return cidade_praia;
	}
	public void setCidade_praia(String cidade_praia) {
		this.cidade_praia = cidade_praia;
	}
	public String getEstado_praia() {
		return estado_praia;
	}
	public void setEstado_praia(String estado_praia) {
		this.estado_praia = estado_praia;
	}
	public Integer getStatus_praia() {
		return status_praia;
	}
	public void setStatus_praia(Integer status_praia) {
		this.status_praia = status_praia;
	}

	
}
