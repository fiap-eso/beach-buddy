package ENTIDADES;

public class Login_ENT {

	public Integer id_login;
	public String nome_user_login;
	public String senha_login;
	
	public Integer getId_login() {
		return id_login;
	}
	public void setId_login(Integer id_login) {
		this.id_login = id_login;
	}
	public String getNome_user_login() {
		return nome_user_login;
	}
	public void setNome_user_login(String nome_user_login) {
		this.nome_user_login = nome_user_login;
	}
	public String getSenha_login() {
		return senha_login;
	}
	public void setSenha_login(String senha_login) {
		this.senha_login = senha_login;
	}

}
